const addTwoNumbers = (firstNumber, secondNumber) => firstNumber + secondNumber;

const substractTwoNumbers = (firstNumber, secondNumber) =>
  firstNumber - secondNumber;

const multiplyTwoNumbers = (firstNumber, secondNumber) =>
  firstNumber * secondNumber;

const divideTwoNumbers = (firstNumber, secondNumber) =>
  firstNumber / secondNumber;

/*named export*/
export {
  addTwoNumbers,
  substractTwoNumbers,
  multiplyTwoNumbers,
  divideTwoNumbers,
};
